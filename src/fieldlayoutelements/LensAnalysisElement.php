<?php

declare(strict_types=1);

namespace vitordiniz22\craftlens\fieldlayoutelements;

use Craft;
use craft\base\ElementInterface;
use craft\elements\Asset;
use craft\fieldlayoutelements\BaseUiElement;
use vitordiniz22\craftlens\helpers\AssetTitleHelper;
use vitordiniz22\craftlens\Plugin;
use vitordiniz22\craftlens\records\DuplicateGroupRecord;
use vitordiniz22\craftlens\web\assets\lens\LensAssetActionsAsset;

class LensAnalysisElement extends BaseUiElement
{
    public static function displayName(): string
    {
        return Craft::t('lens', 'AI Analysis Panel');
    }

    public static function icon(): ?string
    {
        return 'wand-magic-sparkles';
    }

    protected function selectorIcon(): ?string
    {
        return 'wand-magic-sparkles';
    }

    public function isMultiInstance(): bool
    {
        return false;
    }

    protected function selectorLabel(): string
    {
        return Craft::t('lens', 'AI Analysis');
    }

    public function formHtml(?ElementInterface $element = null, bool $static = false): ?string
    {
        if (!$element instanceof Asset) {
            return null;
        }

        if ($element->kind !== Asset::KIND_IMAGE) {
            return null;
        }

        Craft::$app->getView()->registerAssetBundle(LensAssetActionsAsset::class);

        $analysis = Plugin::getInstance()->assetAnalysis->getAnalysis($element->id);
        $settings = Plugin::getInstance()->getSettings();
        $tags = [];
        $colors = [];
        $exifData = null;
        $similarImages = [];

        if ($analysis) {
            $tags = Plugin::getInstance()->tagAggregation->getTagsForAnalysis($analysis->id);
            $colors = Plugin::getInstance()->colorAggregation->getColorsForAnalysis($analysis->id);

            if ($analysis->hasExifMetadata) {
                $exifData = $analysis->exifMetadata;
            }

            $duplicates = DuplicateGroupRecord::find()
                ->where(['and',
                    ['resolvedAt' => null],
                    ['or',
                        ['canonicalAssetId' => $element->id],
                        ['duplicateAssetId' => $element->id],
                    ],
                ])
                ->orderBy(['similarity' => SORT_DESC])
                ->limit(3)
                ->all();

            $otherAssetIds = array_map(function($dup) use ($element) {
                return $dup->canonicalAssetId == $element->id
                    ? $dup->duplicateAssetId
                    : $dup->canonicalAssetId;
            }, $duplicates);

            $otherAssets = Asset::find()
                ->id($otherAssetIds)
                ->indexBy('id')
                ->all();

            foreach ($duplicates as $dup) {
                $otherAssetId = ($dup->canonicalAssetId == $element->id)
                    ? $dup->duplicateAssetId
                    : $dup->canonicalAssetId;

                $otherAsset = $otherAssets[$otherAssetId] ?? null;

                if ($otherAsset !== null) {
                    $thumbnailUrl = Craft::$app->getAssets()->getThumbUrl($otherAsset, 120, 120);

                    if ($thumbnailUrl !== null) {
                        $similarImages[] = [
                            'assetId' => $otherAssetId,
                            'filename' => $otherAsset->filename,
                            'thumbnailUrl' => $thumbnailUrl,
                            'editUrl' => $otherAsset->getCpEditUrl(),
                            'similarity' => $dup->similarity,
                        ];
                    }
                }
            }
        }

        return Craft::$app->view->renderTemplate(
            'lens/_components/analysis-panel.twig',
            [
                'asset' => $element,
                'analysis' => $analysis,
                'settings' => $settings,
                'isAutoGeneratedTitle' => AssetTitleHelper::isAutoGenerated($element),
                'static' => $static,
                'analysisTags' => $tags,
                'analysisColors' => $colors,
                'exifData' => $exifData,
                'similarImages' => $similarImages,
            ]
        );
    }
}
